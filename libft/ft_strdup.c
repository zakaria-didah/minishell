#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*s;
	size_t	len;

	i = 0;
	len = ft_strlen(s1);
	s = (char *)ft_calloc(sizeof(char)* (len + 1), C_ARENA);
	if (!s)
		return (NULL);
	while (s1[i])
	{
		s[i] = s1[i];
		i++;
	}
	s[i] = '\0';
	return (s);
}
